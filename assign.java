class assign {
        public static void main(String args[])
        {
            int a=30;
            System.out.println(a+=30);
            System.out.println(a-=3);
            System.out.println(a*=30);
            System.out.println(a/=30);
            System.out.println(a%=30);
       
        }
    
    }
       
